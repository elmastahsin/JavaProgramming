package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println(" Hello Students\n Today ve have Java Class"); // \n sequence adds new line

        System.out.println("--------------------------------------------");

        System.out.println("\tJava Programming Language");

        System.out.println("\t\t\t\tJava Developer Course Batch 1");

        System.out.println("--------------------------------------------");

        System.out.println("\\");

        System.out.println("-------------------------------------------");

        System.out.println("My favorite Tv show is \" Game of Thrones \" ");




    }
}
/*
    1. \n: New line
    2. \t: Paragraph Space
    3. \\: Single back slash
    4. \": double quote
 */